    public static String getDescriptionByCode(Integer code) {
        for (var item : values()) {
            if (item.getCode().equals(code)) {
                var msg = item.getSolution();
                msg = item.getParaphrase() + (StringUtils.hasText(msg) ? "； 解决方法：" + msg : "");
                msg = item.getDescription() + (StringUtils.hasText(msg) ? "(释义：" + msg + ")" : "");
                return msg;
            }
        }
        return "";
    }
